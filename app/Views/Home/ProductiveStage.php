                    <div id="Category3" class="collapse">
                        <img class="card-img-top"  src="./assets/img/contrato.jpg">
                        <div class="card-body">
                            El contrato de aprendizaje es una forma especial de vinculación dentro del Derecho Laboral, sin subordinación y por un plazo no mayor a dos (2) años en la que una persona natural recibe formación teórica en una entidad de formación autorizada con el auspicio de una empresa patrocinadora que suministra los medios para que adquiera formación profesional metódica y completa requerida en el oficio, actividad u ocupación dentro del manejo administrativo, operativo, comercial o financiero propios del giro ordinario de las actividades del patrocinador con exclusividad en las actividades propias del aprendizaje y el reconocimiento de un apoyo de sostenimiento que garantice el proceso de aprendizaje y el cual, en ningún caso, constituye salario.
                        </div>    
                    </div>
                    
                    <div class="accordion">
                        <div class="card-header">
                            <button class="btn btn-block" type="button" data-toggle="collapse" data-target="#List3-1" aria-expanded="true">
                                <b>Deberes de la Empresa Patrocinadora</b>
                            </button>
                        </div>

                        <div id="List3-1" class="collapse">
                            <ul class="list-group list-group-flush">
                                <li class="list-group-item">
                                    Suministrar al aprendiz los medios para que adquiera formación profesional metódica y completa requerida en el oficio, actividad u ocupación dentro del manejo administrativo, operativo, rural, comercial y financiero inherente a la actividad de la empresa.
                                </li>
                                <li class="list-group-item">
                                    Suscribir el contrato de aprendizaje, teniendo en cuenta las formalidades exigidas en el Decreto 933 de 2003, artículo 2°.
                                </li>
                                <li class="list-group-item">
                                    Garantizar los elementos de protección personal, de acuerdo con el riesgo de la labor que esté desarrollando y
                                </li>
                                <li class="list-group-item">
                                    Reconocer y pagar oportunamente el apoyo de sostenimiento que no constituye salario.
                                </li>
                                <li class="list-group-item">
                                    Afiliar al aprendiz al régimen contributivo de seguridad social en salud y realizar el pago oportunamente. En caso de que este pago se realice a través de la planilla única la empresa debe afiliarlo como aprendiz y establecer la etapa de formación en la que se encuentra (lectiva o productiva).
                                </li>
                                <li class="list-group-item">
                                    Afiliar al aprendiz a una Administradora de Riesgos Laborales – ARL, durante la etapa productiva del contrato de aprendizaje. Exceptuando formación en alternancia o dual, la cual debe realizarse durante todo el proceso de formación.
                                </li>
                                <li class="list-group-item">
                                    En ningún caso la empresa patrocinadora podrá descontar valor alguno al aprendiz del valor de su apoyo de sostenimiento para el pago de las obligaciones de EPS y ARL.
                                </li>
                                <li class="list-group-item">
                                    No vincular personal bajo la modalidad de contrato de aprendizaje, que esté o haya estado vinculado a la empresa. Esta figura no podrá ser utilizada para cubrir vacantes o creación de empleo nuevo ni para reemplazar al personal de las empresas y organismos públicos o privados.
                                </li>
                                <li class="list-group-item">
                                    Programar y gestionar en conjunto con el Instructor asignado por el SENA elseguimiento al aprendiz con Contrato de Aprendizaje.
                                </li>
                            </ul>
                        </div>
